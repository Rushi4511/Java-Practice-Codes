package Java_Basics_Strivers;

public class EvenOdd {
    public static void main(String[] args){

        for(int i=0;i<=10;i++){

            if(i%2==0){

                System.out.println("Number "+i+" is an Even Number");
            } else  {
                System.out.println("Number "+i+" is an Even Number");

            }
        }
    }
}
